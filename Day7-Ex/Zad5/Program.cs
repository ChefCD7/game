    internal class Program
    {
        static void Main(string[] args)
        {
            for (int i = 1; i <= 9; i++)
            {
                for (int j = 1; j <= 9; j++)
                {
                    for (int k = 1; k <= 9; k++)
                    {
                        for (int l = 1; l <= 9; l++)
                        {
                            Console.WriteLine($"{i}{j}{k}{l}");
                        }
                    }
                }
            }

            for (int i = 1111; i < 9999; i++)
            {
                console.writeline(i);
            }
        }
    }
